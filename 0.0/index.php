  <!-- Closing Tag: contains a forward slash after "<". Example: </p> -->
  <!-- Element: consists of an opening tag, a closing tag, and typically something in the middle. Example: <p>Hi!</p> -->
  <!-- Attribute: modifies an element, included in opening tag, has a value. Example: <a href="test.php">Click Me!</a> -->

<!-- HTML encloses content in tags.  Note that a forward slash indicates a closing tag -->
